    {
      System.out.print(i + " ");
    }
    System.out.println();
    //------------------------------------------------------

    //------------------------------------------------------
    //Counting 10 - 0 backwards
    System.out.println("Counting 10 - 0 backwards");
    //Count from 0 - 9
    for (int i=10; i>=0; i--)
    {
      System.out.print(i + " ");
    }
    System.out.println();
